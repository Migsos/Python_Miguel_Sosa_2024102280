def sumar(a,b):
    return a+b

def resta(a,b):
    return a-b
def multp(a,b):
    return a*b
def div(a,b):
    return a/b
def resto(a,b):
    return a%b


n1=3
n2=5
resultado=sumar(n1,n2)
print(f"La suma es: ",{resultado})
resultado=resta(n1,n2)
print(f"La resta es: ",{resultado})
resultado=multp(n1,n2)
print(f"La multp es: ",{resultado})
resultado=div(n1,n2)
print(f"La div es: ",{resultado})
resultado=resto(n1,n2)
print(f"La resto es: ",{resultado})